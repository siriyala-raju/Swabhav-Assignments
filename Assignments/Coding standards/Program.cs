using System;

namespace LoopTest
{
    internal class Program
    {
        static void Main(string[] args)
        {
            for (int i = 1; i <= 5; i++)
            {
                Console.WriteLine("Raju");
            }

            int j = 1;
            while (j <= 5)
            {
                Console.WriteLine("Raju");
                j++;
            }
        }
    }
}